package com.sunsea.parkinghere.framework.edm;

public class SecurityEdmException extends RuntimeException {
    
    public SecurityEdmException() {
        super();
    }
    
    public SecurityEdmException(String message, Throwable cause) {
        super(message, cause);
    }
    
    public SecurityEdmException(String message) {
        super(message);
    }
    
    public SecurityEdmException(Throwable cause) {
        super(cause);
    }
    
}
